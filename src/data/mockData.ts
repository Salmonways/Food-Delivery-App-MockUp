export type MenuItem = {
  id: string;
  name: string;
  price: number; 
  image?: string; 
  description?: string;
  type: 'food' | 'drink' | 'dessert';
};

export type Restaurant = {
  id: string;
  name: string;
  lat: number;
  lng: number;
  image?: string;
  address: string;
  description?: string;
  rating: number;
  reviews: number; 
  menu: MenuItem[];
};

