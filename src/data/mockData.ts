function priceBaht(min: number, max: number) {
  // round to nearest 5 baht
  const p = Math.round((min + Math.random() * (max - min)) / 5) * 5;
  return Math.max(10, p);
}

function makeMenus(
  restaurantIndex: number,
  seedNames: (string | [string, 'food' | 'drink' | 'dessert', any])[] // add optional image param
) {
  const arr: MenuItem[] = seedNames.map((entry, i) => {
    let name: string, type: MenuItem['type'], image: any;

    if (Array.isArray(entry)) {
      [name, type, image] = entry;
    } else {
      name = entry as string;
      type = 'food';
      image = undefined;
    }

    const id = `r${restaurantIndex}-m${i}`;
    const desc = `${name} — a delicious ${type === 'drink' ? 'beverage' : type} that locals enjoy`;

    return {
      id,
      name,
      price: priceBaht(
        type === 'drink' ? 30 : type === 'dessert' ? 50 : 70,
        type === 'drink' ? 70 : type === 'dessert' ? 120 : 250
      ),
      image: image ?? `https://picsum.photos/seed/${encodeURIComponent(id)}/300/200`,
      description: desc,
      type,
    };
  });
  return arr;
}

